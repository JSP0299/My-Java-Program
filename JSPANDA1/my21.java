//MATH CLASS(related to mathematics)
public class my21 {
    public static void main(String[] args){
    //.max ex:- 5,6 max is 6
    //"M" must be in capital(Math)
    int a=5,b=6;
    System.out.println(Math.max(a,b));//max
    System.out.println(Math.min(a,b));//min
    //random(it return 0.0 to 1.0 randomly)
    System.out.println(Math.random());
    System.out.println((int)Math.random());
    System.out.println((int)Math.random()*100);//Math must be in paranthesis
    System.out.println((int)(Math.random()*100));
    }
}
